import { loadRegistry } from "../lib/registry.ts";
import { getComposeStatus } from "../lib/compose.ts";

export async function listCommand(): Promise<void> {
  const reg = await loadRegistry();
  const names = Object.keys(reg.projects);

  if (names.length === 0) {
    console.log("No projects registered. Run: claw-farm init <name>");
    return;
  }

  console.log("\nв”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ");
  console.log("в”Ӯ claw-farm projects                                                                            в”Ӯ");
  console.log("в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө");
  console.log("в”Ӯ Name         в”Ӯ Runtime  в”Ӯ Port    в”Ӯ Status    в”Ӯ Instances  в”Ӯ Path                           в”Ӯ");
  console.log("в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”јв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө");

  const rows = await Promise.all(names.map(async (name) => {
    const entry = reg.projects[name];
    if (!entry) return null; // noUncheckedIndexedAccess guard вҖ” names comes from Object.keys so entry always exists
    const status = await getComposeStatus(entry.path);
    return { name, entry, status };
  }));

  for (const row of rows) {
    if (!row) continue;
    const { name, entry, status } = row;
    const statusIcon = status === "running" ? "рҹҹў" : status === "stopped" ? "вҡӘ" : "вқ“";

    const nameCol = name.length > 12 ? name.slice(0, 9) + "..." : name.padEnd(12);
    const runtimeCol = (entry.runtime ?? "openclaw").padEnd(8).slice(0, 8);
    const portCol = String(entry.port).padEnd(7);
    const statusCol = `${statusIcon} ${status}`.padEnd(9);

    const instanceCount = entry.multiInstance
      ? String(Object.keys(entry.instances ?? {}).length)
      : "-";
    const instanceCol = instanceCount.padEnd(10);

    const pathCol = entry.path.length > 30 ? "..." + entry.path.slice(-27) : entry.path.padEnd(30);

    console.log(`в”Ӯ ${nameCol} в”Ӯ ${runtimeCol} в”Ӯ ${portCol} в”Ӯ ${statusCol} в”Ӯ ${instanceCol} в”Ӯ ${pathCol} в”Ӯ`);
  }

  console.log("в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ");
  console.log(`\n  Total: ${names.length} project(s) | Next port: ${reg.nextPort}\n`);
}
